    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tablaEmpresas.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Debe elegir una fila de la tabla");
            return;
        }
        int res = JOptionPane.showConfirmDialog(this, "¿Desea eliminar empresa?", "Eliminar", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            Empresa e = model.getRow(tablaEmpresas.getSelectedRow());
            try {
                dm.getEmpresaDAO().eliminar(e);
                JOptionPane.showMessageDialog(this, "Empresa eliminada");
                llenarTabla();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Empresa e = new Empresa();
        llenarEmpresa(e);
        if (nuevo) {
            try {
                dm.getEmpresaDAO().insertar(e);
                JOptionPane.showMessageDialog(this, "Empresa agregada");
                llenarTabla();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } else {
            try {
                dm.getEmpresaDAO().modificar(e);
                JOptionPane.showMessageDialog(this, "Empresa Modificada");
                llenarTabla();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "No se pudo actualizar empresa");
            }
        }
        controlarBotones(true);
        controlarTextos(false);
        vaciarTextos();
    }//GEN-LAST:event_btnAceptarActionPerformed

