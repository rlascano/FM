    private void llenarTabla() {
        try {
            model.update();
            model.fireTableDataChanged();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "No se pudo llenar la tabla");
        }
    }

    private void controlarBotones(boolean enabled) {
        btnNuevo.setEnabled(enabled);
        btnEditar.setEnabled(enabled);
        btnEliminar.setEnabled(enabled);
        btnAceptar.setEnabled(!enabled);
        btnCancelar.setEnabled(!enabled);
    }

    private void controlarTextos(boolean enabled) {
        txtId.setEnabled(false);
        txtNombre.setEnabled(enabled);
        txtCuil.setEnabled(enabled);
        txtDireccion.setEnabled(enabled);
        txtLocalidad.setEnabled(enabled);
        txtProvincia.setEnabled(enabled);
        txtPais.setEnabled(enabled);
        txtCP.setEnabled(enabled);
    }

    private void llenarEmpresa(Empresa e) {
        e.setId(Long.parseLong(txtId.getText()));
        e.setNombre(txtNombre.getText());
        e.setCuil(txtCuil.getText());
        e.setDireccion(txtDireccion.getText());
        e.setLocalidad(txtLocalidad.getText());
        e.setProvincia(txtProvincia.getText());
        e.setPais(txtPais.getText());
        e.setCodigoPostal(txtCP.getText());
    }

