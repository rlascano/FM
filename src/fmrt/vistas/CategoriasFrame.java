    private void llenarLista() {
        lmodel.clear();
        try {
            List<Categoria> lista = manager.getCategoriaDAO().listar();
            lista.forEach((c) -> {
                lmodel.addElement(c);
            });
            listaCategorias.setModel(lmodel);
        }catch(SQLException ex) {
            JOptionPane.showMessageDialog(this, "No se pudo cargar lista");
        }    
        
    }
    
    private void botones(boolean estado) {
        btnNuevo.setEnabled(estado);
        btnEditar.setEnabled(estado);
        btnEliminar.setEnabled(estado);
        btnAceptar.setEnabled(!estado);
        btnCancelar.setEnabled(!estado);
    }
    
    private void textos(boolean estado) {
        txtId.setEnabled(false);
        txtNombre.setEnabled(estado);
    }

